package com.example.quotevault.ui.theme

import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

/**
 * Standardized spacing and sizing values for consistent layout throughout the application.
 * 
 * Provides:
 * - Spacing values from XS (4dp) to XXL (48dp)
 * - Border radius values from SM (8dp) to XL (24dp)
 * - Card elevation values for default and hover states
 */
object Dimensions {
    // Spacing values
    val spaceXS: Dp = 4.dp
    val spaceSM: Dp = 8.dp
    val spaceMD: Dp = 16.dp
    val spaceLG: Dp = 24.dp
    val spaceXL: Dp = 32.dp
    val spaceXXL: Dp = 48.dp
    
    // Border radius values
    val radiusSM: Dp = 8.dp
    val radiusMD: Dp = 12.dp
    val radiusLG: Dp = 16.dp
    val radiusXL: Dp = 24.dp
    
    // Elevation values
    val cardElevation: Dp = 2.dp
    val cardElevationHover: Dp = 8.dp
}
